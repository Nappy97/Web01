package Lec09_While.com.lec.java.while03;

public class While03Main {

    public static void main(String[] args) {
        System.out.println("while 연습");

        int n = 1;
        while (n <= 10) {

            if (n % 2 == 1) {
                System.out.println(n);
            }
            n++;
        }

    } // end main()

} // end class While03Main









